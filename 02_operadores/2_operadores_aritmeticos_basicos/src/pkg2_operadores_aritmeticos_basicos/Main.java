public class Main {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        //declaración de variables
        
        int num1 = 10;
        int num2 = 5;
        
        double resultado;
        
        //operadores de suma;
        resultado = num1 + num2;
        System.out.println(resultado);
        
        //operador de resta
        resultado = num1 - num2;
        System.out.println(resultado);
        
        //operador de multiplicación
        resultado = num1 * num2;
        System.out.println(resultado);
        
        //operador de división
        resultado = num1 / num2;
        System.out.println(resultado);
        
        //operador de modulo
        resultado = num1 % num2;
        System.out.println(resultado);
        
        
        /*
        el resultado es de tipo long si al menos uno
        de los operadores es de tipo es de tipo long y ninguo es 
        real (float o double)
        long num3 = 101;
        int num4
        */
        
    }
    
}
